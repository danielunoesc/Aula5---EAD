	public String getVlPag() {
		return VlPag;
	}
	public void setVlPag(String vlPag) {
		VlPag = vlPag;
	}
	public String getDtDev() {
		return dtDev;
	}
	public void setDtDev(String dtDev) {
		this.dtDev = dtDev;
	}
	
