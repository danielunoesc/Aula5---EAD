package Get;

public class Carros{

	private String Codcar;
	private String Nome;
	private String VlCar;
	private String LocCar;
	private String ModCar;
	private String MarCar;
	public String getCodcar() {
		return Codcar;
	}
	public void setCodcar(String codcar) {
		Codcar = codcar;
	}
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getVlCar() {
		return VlCar;
	}
	public void setVlCar(String vlCar) {
		VlCar = vlCar;
	}
	public String getLocCar() {
		return LocCar;
	}
	public void setLocCar(String locCar) {
		LocCar = locCar;
	}
	public String getModCar() {
		return ModCar;
	}
	public void setModCar(String modCar) {
		ModCar = modCar;
	}
	public String getMarCar() {
		return MarCar;
	}
	public void setMarCar(String marCar) {
		MarCar = marCar;
	}	

}
